-- A helper which nubs a list of tuples based on uniqueness of the first element        
nubTup :: Ord a => [(a,b)]  -> [(a,b)] 
nubTup lst = fst $ foldl (\(nubbed_lst,occs) (e1,e2) -> 
    if Set.member e1 occs 
        then 
            (nubbed_lst, occs) 
        else
            (nubbed_lst++[(e1,e2)], Set.insert e1 occs) 
